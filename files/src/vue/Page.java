package vue;

/**
 * L'énumération Page définit les différents modes de l'interface utilisateur
 * du jeu de Hex. Ces modes déterminent la façon dont l'utilisateur interagit
 * avec le jeu.
 */
public enum Page {

	
	/**
     * Le mode accueil présente à l'utilisateur le panneau d'accueil
     * ou le menu principal de l'application.
     */
    ACCEUIL,
    
    /**
     * Le mode Jeu fournit à l'utilisateur un panneau 
     * sous forme d'une grille de jeu avec un bouton suivant qui permet de gérer le jeu
     */
    JEU
	
}
